/// Reusable Modifiers Pack
///
/// A collection of commonly used SwiftUI view modifiers for faster UI development.
///
/// Available modifiers:
/// - `roundedCardShadow()` - Applies rounded corners and shadow for card-like appearance
/// - `pressableScale()` - Adds scale animation on press
/// - `shakeOnError()` - Shakes view when error state is triggered
/// - `skeleton()` - Shows skeleton loading state
/// - `if()` - Conditionally applies view modifiers
///
/// Utility views:
/// - `ConditionalView` - Conditionally displays one of two views
/// - `ConditionalViewOptional` - Conditionally displays a view

